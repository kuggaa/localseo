                        <h2>How it Works</h2>
                        <p class="first">SEO is one of the fastest, easiest and most inexpensive ways to increase awareness of your products, brand and website.  SEO focuses on the volumes of keyword searches relevant to your industry and the linking of those keywords strategically to the most beneficial page of your website to ensure success.  
                        </p>
                        <p class="second">Search Engine Optimization works by building your relevance to searches users are doing on a daily basis through local listing, onsite and offsite optimization.</p>
                                            
                        <p class="second">The first step in building a successful campaign is determining which keywords, as a business, you would like to rank for.  Are those keywords relevant to the products and services you provide?  Will they provide you with traffic for the long term?  What kind of client are they going to drive to your business?  All of these questions should be taken into effect when looking to develop a significant list of keywords for a campaign.  Once you are able to answer these questions with confidence, you can create a list that is going to benefit your business in the ways you are looking to improve.
                        </p>
                        <p>The second aspect, after choosing keywords, is to create the onsite changes to work alongside the keyword optimization.  Onsite optimization in the way of Title Tags, Meta Descriptions and Content uses the keywords you deem important and places them strategically on the site.  All of these areas are crawled by the search engines; therefore, it is important to place them on the most relative pages in an order that is readable to users but informative to the search engines.</p>
                        <p>Once onsite optimization is completed, the offsite can commence.  Offsite optimization comes in the form of a good back-linking strategy which will link your site to others to boost your credibility within the search engines.  By linking your site to others on a daily, but gradual basis, you will be able to build up your rankings and become more visible to those searching for your products or services.
                        </p>
                        <p>While it may be confusing at first, Local SEO keeps things simple and understandable at all times.  Combining a good SEO and Local listing campaign can mean the difference in success for your business.  SEO is a process that does take time; however, Local SEO has the experience and the know-how to increase your business exponentially!
                        </p>
